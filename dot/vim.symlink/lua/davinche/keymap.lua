-- Basic Navigation
vim.keymap.set("v", "<", "<gv", { desc = "Move block left" })
vim.keymap.set("v", ">", ">gv", { desc = "Move block right" })
vim.keymap.set("n", "j", "gj", { desc = "Move down visual line" })
vim.keymap.set("n", "k", "gk", { desc = "Move up visual line" })
vim.keymap.set("n", "dd", "ddk", { desc = "Delete line" })
vim.keymap.set("n", "*", "*``", { desc = "Jump to next word" })
vim.keymap.set("n", "<leader>y", '"+y', { desc = "Copy to clipboard" })
vim.keymap.set("v", "<leader>y", '"+y', { desc = "Copy to clipboard" })
vim.keymap.set("n", "<C-d>", "<C-d>zz", { desc = "Move down half page" })
vim.keymap.set("n", "<C-u>", "<C-u>zz", { desc = "Move up half page" })
vim.keymap.set("n", "<C-n>", "<cmd>cnext<CR>", { desc = "Next quickfix" })
vim.keymap.set("n", "<C-p>", "<cmd>cprev<CR>", { desc = "Previous quickfix" })
vim.keymap.set("n", "-", "<cmd>Oil<CR>", { desc = "Open oil" })

-- Stop Highlighting
vim.keymap.set("n", "<Esc>", "<cmd>nohlsearch<CR>")

-- Diagnostic keymaps
vim.keymap.set("n", "<leader>q", vim.diagnostic.setloclist, { desc = "Open diagnostic [Q]uickfix list" })

-- Block Commenting
local davinche = require("davinche.custom")
vim.keymap.set("n", "<leader>bc", davinche.BlockComment, { desc = "Block Comment" })
vim.keymap.set("n", "<leader>lc", davinche.LineComment, { desc = "Line Comment" })

